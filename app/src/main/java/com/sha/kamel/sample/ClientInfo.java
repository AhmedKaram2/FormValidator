public class ClientInfo {
    private String name;
    private String age;
    private String mobile;
    private String area;

    public ClientInfo setName(String name) {
        this.name = name;
        return this;
    }

    public ClientInfo setAge(String age) {
        this.age = age;
        return this;
    }

    public ClientInfo setMobile(String mobile) {
        this.mobile = mobile;
        return this;
    }

    public ClientInfo setArea(String area) {
        this.area = area;
        return this;
    }
}
